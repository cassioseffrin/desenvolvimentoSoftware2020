package aulas1e2;

class Matematica {

	float multiplica(float a, float b) {
		return a * b;
	}

	double raiz(float n) {
		return Math.sqrt(n);
	}

	float divisao(float n1, float n2) {
		return n1 / n2;
	}

	float adicao(float n1, float n2) {
		return n1 + n2;
	}

	float subtracao(float n1, float n2) {
		return n1 - n2;
	}
}
